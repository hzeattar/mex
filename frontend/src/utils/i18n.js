function readCookie(name) {
  try {
    const m = document.cookie.match(new RegExp('(?:^|; )' + name.replace(/([.$?*|{}()\[\]\\\/+^])/g, '\\$1') + '=([^;]*)'));
    return m ? decodeURIComponent(m[1]) : null;
  } catch (_e) { return null; }
}

function readStorage(name) {
  try { return localStorage.getItem(name); } catch (_e) { return null; }
}

function writeStorage(name, value) {
  try { localStorage.setItem(name, value); } catch (_e) {}
}

function writeCookie(name, value) {
  try {
    document.cookie = `${name}=${encodeURIComponent(value)}; path=/; max-age=31536000; SameSite=Lax`;
  } catch (_e) {}
}

function hasExplicitLocaleChoice() {
  return readStorage(LOCALE_EXPLICIT_STORAGE_KEY) === '1'
    || readCookie(LOCALE_EXPLICIT_STORAGE_KEY) === '1';
}

function initialLocale() {
  if (!hasExplicitLocaleChoice()) return FALLBACK_LOCALE;
  return readStorage(LOCALE_STORAGE_KEY)
    || readStorage(AUTH_LOCALE_STORAGE_KEY)
    || readCookie(LOCALE_STORAGE_KEY)
    || FALLBACK_LOCALE;
}

let locale = initialLocale();
let translations = { ...BUILT_INS.en };

function normalizeLocale(lang) {
  const normalized = String(lang || FALLBACK_LOCALE).slice(0, 2).toLowerCase();
  return SUPPORTED.includes(normalized) ? normalized : FALLBACK_LOCALE;
}

export async function initI18n() {
  locale = normalizeLocale(locale);
  await loadLocale(locale);
}

export async function loadLocale(lang) {
  locale = normalizeLocale(lang);

  const COMMON_LANGS = ['en','ar','ru','tr','es','fr','zh'];
  const authLocale = COMMON_LANGS.includes(locale) ? locale : 'en';
  writeStorage(AUTH_LOCALE_STORAGE_KEY, authLocale);

  let remote = {};
  if (locale !== FALLBACK_LOCALE) {
    try {
      const res = await fetch(`/assets/i18n/${locale}.json`, { cache: 'no-store' });
      if (res.ok) remote = await res.json();
    } catch (_e) {
      remote = {};
    }
  }

  translations = {
    ...BUILT_INS.en,
    ...(remote && typeof remote === 'object' ? remote : {}),
    ...(BUILT_INS[locale] || {}),
  };

  const rtlLangs = ['ar', 'he', 'fa', 'ur'];
  const isRtl = rtlLangs.includes(locale);
  writeStorage(LOCALE_STORAGE_KEY, locale);
  writeCookie(LOCALE_STORAGE_KEY, locale);
  document.documentElement.lang = locale;
  document.documentElement.dir = isRtl ? 'rtl' : 'ltr';
  document.body?.classList.toggle('rtl', isRtl);
  document.body?.classList.toggle('ltr', !isRtl);
  return translations;
}

export function t(key, fallback = '') {
  const value = translations[key] || BUILT_INS.en[key];
  if (value) return value;
  const text = fallback || key;
  if (locale !== FALLBACK_LOCALE && text) return (PHRASES[locale] || {})[text] || text;
  return text;
}

export function currentLocale() {
  return locale;
}

export function isRTL() {
  return ['ar', 'he', 'fa', 'ur'].includes(locale);
}

export function setLocale(lang, options = {}) {
  const shouldReload = options.reload !== false;
  writeStorage(LOCALE_EXPLICIT_STORAGE_KEY, '1');
  writeCookie(LOCALE_EXPLICIT_STORAGE_KEY, '1');
  return loadLocale(lang).then(() => {
    try {
      window.dispatchEvent(new CustomEvent('vp:localechange', { detail: { locale } }));
    } catch (_e) {}
    if (shouldReload) window.location.reload();
    return locale;
  });
}

export function translateDom(root = document) {
  const keyedPhrases = {};
  for (const [key, english] of Object.entries(BUILT_INS.en)) {
    const translated = translations[key];
    if (translated && translated !== english) keyedPhrases[english] = translated;
  }
  let phraseMap = { ...keyedPhrases, ...(PHRASES[locale] || {}) };

  // When locale is English but the DOM was rendered in Arabic, we need to
  // reverse-map Arabic text back to English so translateDom can fix it.
  if (locale === FALLBACK_LOCALE && Object.keys(phraseMap).length === 0) {
    // Build reverse map from Arabic PHRASES and BUILT_INS.ar
    const arPhrases = PHRASES['ar'] || {};
    for (const [eng, ar] of Object.entries(arPhrases)) {
      if (ar && ar !== eng) phraseMap[ar] = eng;
    }
    for (const [key, english] of Object.entries(BUILT_INS.en)) {
      const ar = BUILT_INS.ar?.[key];
      if (ar && ar !== english) phraseMap[ar] = english;
    }
  }

  if (!Object.keys(phraseMap).length) return;
  const target = root instanceof Element || root instanceof Document ? root : document;
  const walker = document.createTreeWalker(target, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;
      if (!parent) return NodeFilter.FILTER_REJECT;
      if (parent.closest('[data-no-i18n],script,style,textarea,input,select')) return NodeFilter.FILTER_REJECT;
      const text = node.nodeValue || '';
      const trimmed = text.trim();
      if (!trimmed || trimmed.length > 120 || !phraseMap[trimmed]) return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    },
  });

  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  for (const node of nodes) {
    const text = node.nodeValue || '';
    const trimmed = text.trim();
    node.nodeValue = text.replace(trimmed, phraseMap[trimmed]);
  }
}

