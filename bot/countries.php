// Telegram inline keyboards have button limits, so we paginate.
function mex_countries_keyboard(string $lang, int $page = 0, int $perPage = 40): array {
  $items = mex_countries_sorted($lang);

  $total = count($items);
  $perPage = max(10, min(60, $perPage));
  $pages = max(1, (int)ceil($total / $perPage));
  $page = max(0, min($pages-1, $page));
  $start = $page * $perPage;
  $slice = array_slice($items, $start, $perPage);

  $rows = [];
  $row = [];
  foreach ($slice as $it) {
    $cc = (string)($it['code'] ?? '');
    $label = (string)($it['label'] ?? '');
    $txt = trim(mex_flag_emoji($cc).' '.$label);
    $row[] = ['text'=>$txt, 'callback_data'=>'country:'.$cc];
    if (count($row) >= 2) { $rows[]=$row; $row=[]; }
  }
  if ($row) $rows[] = $row;

  // nav
  $nav = [];
  if ($page > 0) $nav[] = ['text'=>'⬅️', 'callback_data'=>'countries:'.($page-1)];
  $nav[] = ['text'=>($page+1).'/'.$pages, 'callback_data'=>'noop'];
  if ($page < $pages-1) $nav[] = ['text'=>'➡️', 'callback_data'=>'countries:'.($page+1)];
  $rows[] = $nav;

  return ['inline_keyboard'=>$rows];
}
