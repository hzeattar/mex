function mex_flag_emoji(string $cc): string {
  $cc = strtoupper($cc);
  if (strlen($cc) !== 2) return '';
  $a = ord($cc[0]) - 65 + 0x1F1E6;
  $b = ord($cc[1]) - 65 + 0x1F1E6;
  return mb_convert_encoding('&#'.$a.';&#'.$b.';', 'UTF-8', 'HTML-ENTITIES');
}

function mex_country_label(string $cc, string $lang): string {
  global $MEX_COUNTRIES_EN, $MEX_COUNTRIES_AR, $MEX_COUNTRIES_RU;
  $cc = strtoupper($cc);
  if ($lang === 'ar' && isset($MEX_COUNTRIES_AR[$cc])) return $MEX_COUNTRIES_AR[$cc];
  if ($lang === 'ru' && isset($MEX_COUNTRIES_RU[$cc])) return $MEX_COUNTRIES_RU[$cc];
  return $MEX_COUNTRIES_EN[$cc] ?? $cc;
}

function mex_sort_key(string $s, string $lang): string {
  $s = trim($s);
  if ($s === '') return '';
  // Best-effort locale sorting without intl extension.
  if ($lang === 'ar') {
    // remove leading "ال"
    if (mb_substr($s, 0, 2, 'UTF-8') === 'ال') $s = mb_substr($s, 2, null, 'UTF-8');
    // normalize common Arabic variants for sorting
    $s = str_replace(['أ','إ','آ','ٱ'], 'ا', $s);
    $s = str_replace(['ى'], 'ي', $s);
    $s = str_replace(['ؤ'], 'و', $s);
    $s = str_replace(['ئ'], 'ي', $s);
    $s = str_replace(['ة'], 'ه', $s);
    // strip tashkeel
    $s = preg_replace('/[\x{064B}-\x{065F}\x{0670}\x{06D6}-\x{06ED}]/u', '', $s);
  } elseif ($lang === 'ru') {
    $s = mb_strtolower($s, 'UTF-8');
    $s = str_replace('ё', 'е', $s);
  } else {
    $s = mb_strtolower($s, 'UTF-8');
  }
  return $s;
}

function mex_countries_sorted(string $lang): array {
  global $MEX_COUNTRIES_EN;
  $lang = in_array($lang, ['en','ar','ru'], true) ? $lang : 'en';

  $list = [];
  foreach ($MEX_COUNTRIES_EN as $cc => $enName) {
    $cc = strtoupper((string)$cc);
    if (!preg_match('/^[A-Z]{2}$/', $cc)) continue;
    $label = mex_country_label($cc, $lang);
    $list[] = ['code' => $cc, 'label' => $label];
  }

  usort($list, function($a, $b) use ($lang) {
    $ka = mex_sort_key((string)$a['label'], $lang);
    $kb = mex_sort_key((string)$b['label'], $lang);
    $cmp = strcmp($ka, $kb);
    if ($cmp !== 0) return $cmp;
    return strcmp((string)$a['code'], (string)$b['code']);
  });

  return $list;
}

